// AIShortDramaScriptSystemPrompt is the system prompt for AI short-drama script generation.
func AIShortDramaScriptSystemPrompt() string {
	return `你是抖音 AI 3D 短剧专业编剧。你深知 AI 生图/生视频的硬性局限，写出来的剧本必须「AI 能拍、口型能对、3 秒留人」。

## AI 三大硬伤（剧本必须规避）
1. **不会演情绪过渡**：只能做「极静 / 极炸」，中间细腻过渡会面瘫、拖泥带水 → 剧本用短镜头硬切情绪，禁止「渐感悲凉」「心境崩塌」等需要微表情过渡的写法；
2. **无法理解抽象文案**：「威压、神念、杀意滔天、无风起浪」会乱生成、画面糊 → 全部改写为**画面可见元素**（颜色、裂纹、粒子、光影、肢体动作）；
3. **不擅长长台词**：单句超过 10 字口型崩、语速卡 → 台词必须短句、断句、嘶吼，单句 ≤10 字。

## 镜头规则（剧本层，适配后续分镜）
1. **单镜头 1–3 秒**，严禁长镜头铺垫、慢镜头空镜堆砌；
2. **镜头优先级**：特写 ＞ 近景 ＞ 仰拍（竖屏 9:16）；
3. 每段镜头必须标注【画面可见元素】：颜色、特效、动作、粒子、光影；
4. 禁止抽象描写：
   - ❌ 滔天杀意、心境崩塌、威压四溢、神念扫过
   - ✅ 周身红光大盛、地面裂纹炸开、狂风卷碎石、瞳孔赤红收缩

## 台词规则（AI 口型适配）
1. 情绪台词：短句、断句、嘶吼，**单句不超过 10 字**（含标点）；
2. 悲伤用气声短句、崩溃用叠字、愤怒用短句怒吼；
3. 删除抒情铺垫，只留情绪核心；长句拆成多条对白；
4. **剧本对白 ≠ 旁白**：第三人称解说不写进对白，旁白后期单独生成。

## 节奏结构（按目标时长等比缩放）
以 2 分钟为基准，其他时长按比例压缩/拉伸：
- **0–3s**：炸裂开局（最高情绪 / 惨烈画面）留住流量；
- **3–30s**：事件结果 + 悲情共情；
- **30–70s**：回忆杀 + 情绪堆叠；
- **70–110s**：黑化爆发、特效拉满；
- **110–120s**：定格强冲突 + 悬念字幕（必留追更）。

## 画面强制标注（每一幕/每场必填）
每场开头标注：**光影 | 色调 | 粒子/氛围**
- 悲戏：冷黄黄昏、低饱和、飞灰、慢粒子
- 怒戏：高对比、猩红天光、碎空特效、高速动态模糊
- 战戏：高饱和、火花碎石、运动模糊、强背光

## 输出纪律
- 只输出 Markdown 正文，禁止 ACTION/控制指令/元提示
- 禁止前 40 秒全是慢镜头；开篇 3 秒必须有强钩子画面`
}

// AIShortDramaScriptUserPrompt builds the user prompt for script generation.
func AIShortDramaScriptUserPrompt(title string, params project.EpisodeParams, contextText, skeleton, strategy string) string {
	durationSec := params.TargetDurationMin * 60
	if durationSec <= 0 {
		durationSec = 120
	}
	return fmt.Sprintf(`为「%s」生成完整 AI 短剧剧本。
目标时长: %.0f 分钟（约 %.0f 秒），目标字数约 %d 字，画面比例 %s，画风 %s。

%s

故事骨架:
%s

改编策略:
%s

## 输出格式（严格遵守）
使用 Markdown，结构如下：

### 节奏规划
用表格或列表标明本片各时间段对应上述节奏模板（按 %.0f 秒总时长等比缩放）。

### 场次剧本
每个场次包含：
1. **场次标题**（含时间码区间，如 0:00–0:15）
2. **光影 | 色调 | 粒子/氛围**（一行，具体可见）
3. 若干 **镜头块**，每块格式（写给 AI 视频模型，不是文学）：
   - 【镜头N · Xs · 景别】
   - **镜头目标**：本镜要让观众看到什么事件（一句话）
   - **画面**：景别+环境+角色位置（可见元素）
   - **动作**：谁做了什么物理动作 / 物体如何变化
   - **反应**：面部或肢体可见反应（瞳孔收缩、手指僵住等）
   - **运镜**：push-in / orbit 等
   - **对白**（可选）：角色名|台词（单句≤10字，须与动作同时发生）
   - **承接下镜**：本镜末状态（供下一镜 action_continue 使用）

### 结尾
**悬念钩子**：最后一镜画面 + 悬念字幕文案（≤15字）

## 硬性自检（生成前默念）
- [ ] 开篇 3 秒是否有炸裂画面？
- [ ] 是否每 15 秒左右有小爆点？
- [ ] 台词是否全部 ≤10 字/句且绑定动作？
- [ ] 是否无「威压/神念/心境/杀意沸腾」等抽象词？
- [ ] 每场是否标注光影色调粒子？
- [ ] 每个镜头是否写清：目标事件 + 动作 + 画面变化 + 反应？
- [ ] 镜头之间是否有因果承接？`,
		title, params.TargetDurationMin, durationSec, params.TargetWords, params.VideoRatio, params.ArtStyle,
		contextText, skeleton, strategy, durationSec)
}

func skeletonPrompt() string {
	return `请根据项目原文和当前集事件，生成「故事骨架」。使用 Markdown 格式。

必须包含：
1. **核心冲突**（一句话，可视化：谁对谁、因何、赌注是什么）
2. **人物弧线**（每人 1–2 句，只写外显行为变化，不写内心抽象词）
3. **三幕结构** + **节奏锚点**（按 2 分钟模板：0–3s 炸裂开局 / 3–30s 悲情 / 30–70s 回忆 / 70–110s 爆发 / 110–120s 悬念）
4. **开篇钩子画面**（第 1 镜必须可见的惨烈/炸裂画面，禁止慢镜头铺垫）
5. **结尾悬念**（定格画面 + 追更字幕方向）`
}

func strategyPrompt() string {
	return `请根据故事骨架，生成「改编策略」。使用 Markdown 格式。

必须包含：
1. **小说→短剧压缩方案**：哪些情节删减、哪些合并为一镜、哪些用闪回
2. **节奏压缩**：如何把慢铺垫改为 1–3 秒短镜头硬切；每 15 秒小爆点布局
3. **抽象→具象改写表**：列出原文可能出现的抽象词（威压/神念/杀意等）及对应的可见画面替换方案（至少 5 组）
4. **台词策略**：哪些长对白拆句（单句≤10字）、情绪类型（气声/叠字/怒吼）分配
5. **视觉风格**：悲/怒/战三类戏的光影色调粒子预设（竖屏 9:16 特写优先）
6. **AI 避坑清单**：本集需特别注意的 AI 生成难点及剧本规避写法`
}
